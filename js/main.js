$(document).ready(function() {

	$('.slider').cycle({
		fx: 'none',
		timeout: 1500,
		duration: 1500
	});

	$('#open-modal').fancybox({
		closeBtn: false,
		autoCenter: true,
		margin: 100,
		padding: 40
	});

	$('#modal .close').click(function(e) {
		e.preventDefault();
		$.fancybox.close();
	});

});
